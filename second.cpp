//wap in switch case statement to taking input in string //wap to taking intput in integer 

#include<iostream>
using namespace std; 
int main(){
     char ch ='2';
     cout<<endl;

    switch(ch){

        cout<<endl; 
         case '1': cout<<"first number"<<endl;
         break;

         case '2': cout<<"second number"<<endl;
         break;

         case '3':cout<<"third number"<<endl;
         break;

         default:cout<<"this is the default value"<<endl;


    }

    cout<<endl;
    return 0;
}